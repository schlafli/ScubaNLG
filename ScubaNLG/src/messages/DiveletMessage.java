package messages;

public class DiveletMessage extends Message {
	
	private int	diveletNumber;
	private int	totalDivelets;
	
	public int getDiveletNumber() {
		return diveletNumber;
	}
	
	public void setDiveletNumber(int diveletNumber) {
		this.diveletNumber = diveletNumber;
	}
	
	public int getTotalDivelets() {
		return totalDivelets;
	}
	
	public void setTotalDivelets(int totalDivelets) {
		this.totalDivelets = totalDivelets;
	}
	
}
